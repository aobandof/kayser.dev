CREATE TABLE sale(
	boleta INT PRIMARY KEY,
	date DATETIME	
)
GO
CREATE TABLE sku(
	code VARCHAR(30) PRIMARY KEY,
	article VARCHAR(20),
	itemname VARCHAR(50),
	colour VARCHAR(20),
	talla VARCHAR(10),
	price FLOAT
)
GO
CREATE TABLE sale_sku (
	sale_boleta INT FOREIGN KEY REFERENCES sale (boleta),
	sku_code VARCHAR(30) FOREIGN KEY REFERENCES sku (code),
	cant INT,
	price FLOAT
)

